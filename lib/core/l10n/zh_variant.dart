/// Returns [input] with every known Traditional character replaced by its
/// Simplified counterpart.
String foldToSimplified(String input) {
  if (input.isEmpty) {
    return input;
  }
  final buffer = StringBuffer();
  for (final char in input.split('')) {
    buffer.write(_traditionalToSimplified[char] ?? char);
  }
  return buffer.toString();
}
